function divide(x, y) {
    return x / y;
};
console.log(divide(6, 3));
console.log(divide(18, 2));
console.log(divide(60, 3));
console.log(divide(21, 7));

function product(x, y) {
    return x - y;
};
console.log(product(6, 3));
console.log(product(18, 2));
console.log(product(60, 3));
console.log(product(21, 7));

function power(x, y) {
    return x**y;
}
console.log(power(6, 3));
console.log(power(18, 2));
console.log(power(60, 3));
console.log(power(21, 7));
